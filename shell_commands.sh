Linux  Komutlari

1) pwd          :   hangi dosyada bulundugunuzu gosterir.
2) ls -a        :   gizli dosyalar dahil tüm dosyalari listelenir.
3) mkdir        :   yeni klasor acar.
4) touch        :   dosya olusturur.
5) cp           :   klasore dosya kopyalar.
6) cd           :   klasore gecis yapilir.
7) rm           :   dosyayi siler.
8) echo         :   dosya acip icine yazi yazilir.
9) cat          :   dosyanin icerigi gösterilir.
10) grep        :   dosyada arama yapilir.   
11) cat         :   iki dosya birlestirilir.   
12) cd ..       :   bir ust dizine cikilir.
13) rm -r       :   dolu olan klasör icidekiler ile birlikte silinir.
14) sudo su     :   root a gecis yapilir